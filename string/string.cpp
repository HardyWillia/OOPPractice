//String class manipulation and practice

#include <iostream>

int main(){

	//Default construct
	string s0;

	//Construct and assign from a literal value
	string s1 = "hello";
	s1 = "foo";

	//Copy construct and assign
	string s2 = s1;
	s2 = s1;

	//Compare  
	s1 = s1;

	//Order
	s1 <s1;

	//Object independence
	s2 = "bar";





	return 0;

}
